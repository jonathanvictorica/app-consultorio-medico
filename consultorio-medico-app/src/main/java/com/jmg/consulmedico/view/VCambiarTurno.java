    private boolean validardatos() {
        if(this.cboTipoTurno.getSelectedIndex()==-1)
        {
            JOptionPane.showMessageDialog(panelImage1, "No seleccionÃ³ el tipo de turno que desea cambiar");
            return false;
        }
         if(this.cboTurno.getSelectedIndex()==-1)
        {
            JOptionPane.showMessageDialog(panelImage1, "No seleccionÃ³ el turno que desea cambiar");
            return false;
        }
         
          if(this.cboFechas.getSelectedIndex()==-1)
        {
            JOptionPane.showMessageDialog(panelImage1, "No seleccionÃ³ la fecha que desea cambiar el turno.");
            return false;
        }
         if(this.cboHorarios.getSelectedIndex()==-1)
        {
            JOptionPane.showMessageDialog(panelImage1, "No seleccionÃ³ la hora que desea cambiar el turno.");
            return false;
        }
        
        
        
        
        
        return true;
    }

    
