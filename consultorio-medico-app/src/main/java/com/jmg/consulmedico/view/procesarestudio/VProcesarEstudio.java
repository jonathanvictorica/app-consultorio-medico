    private boolean validardatos() {
        if(this.cboEspecialista.getSelectedIndex()==-1)
        {
            JOptionPane.showMessageDialog(panelImage1, "Debe seleccionar el mÃ©dico para elegir la fecha y hora del turno que desea atender.");
            return false;
        }
        if(this.cboFecha.getSelectedIndex()==-1)
        {
            JOptionPane.showMessageDialog(panelImage1, "Debe seleccionar la fecha y hora del turno.");
            return false;
        }
        if(this.cboHora.getSelectedIndex()==-1)
        {
            JOptionPane.showMessageDialog(panelImage1, "Debe seleccionar la hora del turno.");
            return false;
        }
        if(this.txtFechaRetiro.getText().equals(""))
        {
            JOptionPane.showMessageDialog(panelImage1, "No ha ingresado la fecha de retiro");
            return false;
        }
        return true;
    }
