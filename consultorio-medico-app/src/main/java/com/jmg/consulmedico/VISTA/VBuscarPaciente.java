    private boolean validardatos() {
       if(this.cboBusqueda.getSelectedIndex()==-1)
        {
             JOptionPane.showMessageDialog(panelImage1, "No ha seleccionado el tipo de busqueda con el que desea encontrar al paciente.");
            return false;
        }
       if(this.txtBusqueda.getText().equals(""))
        {
             JOptionPane.showMessageDialog(panelImage1, "No ha ingresado el " + this.lblBusqueda.getText() + " del paciente.");
            return false;
        }
       return true;
    
    }
