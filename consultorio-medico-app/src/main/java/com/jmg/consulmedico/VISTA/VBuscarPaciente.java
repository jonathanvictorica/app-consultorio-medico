    private void cmdBuscarPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdBuscarPacienteActionPerformed
      if(this.validardatos())
      {
        switch((String)this.cboBusqueda.getSelectedItem())  
         {
             case "Dni":
                 this.paciente=Paciente.buscarPorDNI(this.txtBusqueda.getText());
                 break;
             case "Código de Historial Clínico":
                 this.paciente=Paciente.buscarPorCodigoHistoriaClinica(this.txtBusqueda.getText());
                  break;
             case "Nombre y Apellido":
                  this.paciente=Paciente.buscarPorNombreyApellido(this.txtBusqueda.getText()); 
                 
                 break;
         }
         
         if(this.paciente==null)
         {
             JOptionPane.showMessageDialog(panelImage1, "Este paciente no existe.");
         }
         else
         {
             switch(this.redireccionamiento)
             {
                 case "Cancelar Turno":
                     VCancelarTurno cancelarturno = new VCancelarTurno(this.paciente,false);
                     break;
                 case "Procesar Visita":
                     VProcesarVisita procesarvisita = new VProcesarVisita(this.paciente);
                     break;
                 case "Procesar Estudio":
                     VProcesarEstudio procesarestudio = new VProcesarEstudio(this.paciente);
                     
                     break;
                 case "Cambiar Turno":
                      VCambiarTurno cambiarturno = new VCambiarTurno(this.paciente);
                     break;
             }
             
             
             
             
             
             this.hide();
         }
      }
    }//GEN-LAST:event_cmdBuscarPacienteActionPerformed

